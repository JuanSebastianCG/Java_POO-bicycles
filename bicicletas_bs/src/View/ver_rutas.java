        String mes = String.valueOf(fecha.charAt(8));
         mes = mes +""+ String.valueOf(fecha.charAt(9));
         
        String hora = String.valueOf(fecha.charAt(11));
         hora = hora +""+ String.valueOf(fecha.charAt(12));
         
         System.out.println(""+mes+ "   "+hora);
         
         int mesN = Integer.parseInt(mes);
         int hotaN = Integer.parseInt(hora);
        
        
        String barrio = txt_Barrio.getText();
        String idRuta = txt_idRuta.getText();
        int km = Integer.parseInt(txt_kilometros.getText()) ;
        int star_date = Integer.parseInt(txt_DI.getText());
        int end_date =  Integer.parseInt(txt_DF.getText());
        int star_hour = Integer.parseInt(txt_HI.getText());
