  void show_route(){
        String query = "SELECT * FROM tb_route";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(query);
            Object[] schedule = new Object[7];
            model = (DefaultTableModel)tbl_schedule.getModel();
            while(rs.next()){
                 schedule[0] = rs.getString("barrio");
                 schedule[1] = rs.getString("idRuta");
                 schedule[2] = rs.getInt("km");
                 schedule[3] = rs.getInt("star_date");
                 schedule[4] = rs.getInt("end_date");
                 schedule[5] = rs.getInt("star_hour");
                 schedule[6] = rs.getInt("end_hour");
                model.addRow(schedule);
            }
            tbl_schedule.setModel(model);
        }catch(SQLException e){
            JOptionPane.showConfirmDialog(this, "No se encontraron registros de rutas");
        }
    }


    

    
    void clear_rows_table(){
        for (int i = 0; i < tbl_schedule.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }
        txt_Barrio.setText("");
        txt_idRuta.setText("");
        txt_kilometros.setText("");
        txt_DF.setText("");
        txt_DI.setText("");
        txt_HI.setText("");
        txt_HF.setText("");
    }



    void delete_route(){
      int row_schedule = tbl_schedule.getSelectedRow();
            if(row_schedule  < 0){
                 JOptionPane.showMessageDialog(this, "Debes de seleccionar un registro de la tabla");
            }else{
      
            String idRuta = txt_idRuta.getText();
            if(row_schedule == -1)
                JOptionPane.showMessageDialog(this, "Debes de seleccionar un registro de la tabla");
                else{
                    String sql = "DELETE FROM `tb_route` WHERE idRuta = "+idRuta;
                    try{
                      
                        cn = con.getConnection();
                        st = cn.createStatement();
                        st.executeUpdate(sql);
                        clear_rows_table();
                        JOptionPane.showMessageDialog(this, "Se ha eliminado el registro");
                        show_route();
                    }catch(SQLException e){
                        JOptionPane.showMessageDialog(this, "No se pudo eliminar la información del registro");
                    }
                }
       }
    }
    

    
